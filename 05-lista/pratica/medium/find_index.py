# Lista original com algumas letras
lst = ['A', 'P', 'B', 'Q', 'C', 'D']

"""A função index() encontra o índice do primeiro elemento que corresponde ao valor fornecido
Aqui, estamos procurando pelo valor 'B' dentro da lista lst.
O método index('B') retorna o índice (posição) do primeiro item 'B' na lista.
Como 'B' está na posição 2 (lembrando que a contagem de índices começa do 0), o resultado será 2."""
print(f"Index of B: {lst.index('B')}")
