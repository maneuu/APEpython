myList = ['p','y','t','h','o','n','i','s','t','a']

# elements from 5 to 8
print(myList[4:8])

# from 1st element to 4th element
print(myList[:4])

# from 8th element to last element 
print(myList[7:])

# from 1st to last elements
print(myList[:])

# negative slicing
print(myList[-1:-9])

print(myList[-5:])

print(myList[:-4])